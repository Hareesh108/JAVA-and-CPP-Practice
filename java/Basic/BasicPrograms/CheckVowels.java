package BasicPrograms;

public class CheckVowels {
    public static void main(String[] args) {
        char ch = 'e';

        if(ch == 'a'){
            System.out.println("Vowel");
        }
        else if (ch == 'e') {
            System.out.println("Vowel");
        }
        else if (ch == 'i') {
            System.out.println("Vowel");
        }
        else if (ch == '0'){
            System.out.println("Vowel");
        }
        else if(ch == 'u'){
            System.out.println("Vowel");
        }
        else {
            System.out.println("Not Vowel");
        }

    }
}

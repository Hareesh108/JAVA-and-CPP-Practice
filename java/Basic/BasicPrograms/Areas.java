package BasicPrograms;

public class Areas {
    public static void main(String[] args) {
        //Area Of Circle
        int redius = 5;
        double result = 3.14 * redius * redius;
        System.out.println(result);

        //Area of Square
        int length = 10;
        int res = length * length;
        System.out.println(res);

        //Area of Rectangle
        int l = 15;
        int w = 10;
        int res1 = 2 * l * w;
        System.out.println(res1);
    }
}

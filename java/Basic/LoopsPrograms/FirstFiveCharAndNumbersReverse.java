package LoopsPrograms;

public class FirstFiveCharAndNumbersReverse {
    public static void main(String[] args) {

        int n = 5;
        while (n>=0){
            System.out.print(n + " ");
            n--;
        }
        System.out.println();
        char ch = 'z';
        while (ch>='u' ){
            System.out.print(ch + " ");
            ch--;
        }
    }
}

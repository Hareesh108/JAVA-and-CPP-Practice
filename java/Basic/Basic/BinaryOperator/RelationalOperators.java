package Basic.BinaryOperator;

public class RelationalOperators {
    public static void main(String[] args) {
        //Result will be only conditional true/false
        int a = 4;
        int b = 2;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}

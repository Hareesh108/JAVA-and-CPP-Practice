package Basic.BinaryOperator;

public class ArithmeticOperators {
    public static void main(String[] args) {
        // Result will come in numbers only
        int a = 4;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
    }
}

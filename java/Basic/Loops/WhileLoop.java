package Loops;

public class WhileLoop {
    public static void main(String[] args) {
        int n = 0;
        while (n<=5){
            System.out.print(n + " ");
            n++;
        }
        System.out.println();
        char ch = 'a';
        while (ch<='f'){
            System.out.print(ch + " ");
            ch++;
        }
    }
}
